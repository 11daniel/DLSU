#include <stdio.h>

int main(){
    printf("%d\n",(5 > 4));
    printf("%d\n",(5 >= 5));
    printf("%d\n",(4 < 3));
    printf("%d\n",(6 == 5+1));
    printf("%d\n",(5 != 6));
    printf("%d\n",!(5 >= 5));
    return 0;
}

